'use client';

import {
  Container,
  Title,
  Grid,
  Paper,
  Group,
  Badge,
  Select,
  Stack,
  Text,
  Button,
  Timeline,
  Divider,
  Box,
  ActionIcon
} from '@mantine/core';
import { IconChevronLeft, IconChevronRight } from '@tabler/icons-react';
import { useState, useEffect } from 'react';
import TicketTable from '@/app/components/TicketTable';
import ContactSearch from '@/app/components/ContactSearch';
import { type Ticket } from '@/app/components/ticket-utils';

// TODO: /tickets/123 doesn't work, we should make sure the url reflects the current ticket
// TODO: Rename reaches/calls to use Ticket as name
export default function TicketPage() {
  const [reaches, setTicketes] = useState<Ticket[]>([]);
  const [selectedTicket, setSelectedTicket] = useState<Ticket | null>(null);
  const [loading, setLoading] = useState(true);
  const [status, setStatus] = useState('in-progress');
  const [priority, setPriority] = useState('p3');
  const [assignee, setAssignee] = useState('admin');
  const [nextUrl, setNextUrl] = useState<string | null>(null);
  const [previousUrl, setPreviousUrl] = useState<string | null>(null);
  const [totalCount, setTotalCount] = useState(0);

  const statuses = ['all', 'open', 'in-progress', 'blocked', 'completed'];

  useEffect(() => {
    fetchTicketes();
  }, []);

  const handleReset = () => {
    fetchTicketes();
  };

  const fetchTicketes = async (url?: string) => {
    try {
      setLoading(true);
      const fetchUrl = url || '/api/tickets/';
      const response = await fetch(fetchUrl);
      console.log('Fetch response:', response);
      const data = await response.json();
      console.log('Fetched reaches data:', data);
      setTicketes(data.results);
      setTotalCount(data.count);
      setNextUrl(data.next);
      setPreviousUrl(data.previous);
    } catch (error) {
      console.error('Error fetching reaches:', error);
    } finally {
      setLoading(false);
    }
  };

  const handleRowClick = (reach: Ticket) => {
    setSelectedTicket(reach);
  };

  const handleBackToTable = () => {
    setSelectedTicket(null);
  };


  // TODO: Use code in TicketesTable.tsx
  const getPriorityColor = (priority: number) => {
    if (priority < 1) return 'red';
    if (priority < 3) return 'orange';
    if (priority == 3) return 'yellow';
    if (priority <= 5) return 'gray';
    return 'gray';
  };

  // TODO: Use code in TicketesTable.tsx
  const getStatusColor = (status: string) => {
    switch (status) {
      case 'OPEN': return 'gray';
      case 'TODO': return 'gray';
      case 'IN_PROGRESS': return 'blue';
      case 'BLOCKED': return 'red';
      case 'COMPLETED': return 'DimGray';
      case 'CANCELED': return 'red';
      default: return 'DimGray';
    }
  };

  return (
    <Container size="xl" py="xl">
      <Grid>
        {/* Main Content Area */}
        <Grid.Col span={{ base: 12, md: selectedTicket ? 8 : 12 }}>
          <Stack gap="md">
            {/* Header */}
            <Group justify="space-between">
              <Title order={2}>
                {selectedTicket ? selectedTicket.title : 'Ticket Queue Management'}
              </Title>
              {selectedTicket && (
                <Button variant="outline" onClick={handleBackToTable}>
                  Back to List
                </Button>
              )}
            </Group>

            {/* Status Filters */}
            {!selectedTicket && (
              <Paper p="md" withBorder>
                <Stack gap="md">
                  <Group gap="xs">
                    {statuses.map((s) => (
                      <Badge
                        key={s}
                        variant={status === s ? 'filled' : 'light'}
                        style={{ cursor: 'pointer', textTransform: 'capitalize' }}
                        onClick={() => setStatus(s)}
                      >
                        {s.replace('-', ' ')}
                      </Badge>
                    ))}
                  </Group>

                  <Group gap="md">
                    <Select
                      label="Priority"
                      value={priority}
                      onChange={(value) => setPriority(value || 'p3')}
                      data={[
                        { value: 'p1', label: 'P1 - Critical' },
                        { value: 'p2', label: 'P2 - High' },
                        { value: 'p3', label: 'P3 - Normal' },
                        { value: 'p4', label: 'P4 - Low' },
                      ]}
                      style={{ flex: 1 }}
                    />
                    <Select
                      label="Assignee"
                      value={assignee}
                      onChange={(value) => setAssignee(value || 'admin')}
                      data={[
                        { value: 'admin', label: 'admin (admin@test.com)' },
                        { value: 'user1', label: 'User 1 (user1@test.com)' },
                        { value: 'user2', label: 'User 2 (user2@test.com)' },
                      ]}
                      style={{ flex: 1 }}
                    />
                    <Button mt="xl">Update</Button>
                  </Group>
                <Group gap="sm">
                  <Button variant="outline" onClick={handleReset}>Reset</Button>
                </Group>
                </Stack>
              </Paper>
            )}

            {/* Tickets Table or Call Instructions */}
              <>
                <TicketTable
                  tickets={reaches}
                  loading={loading}
                  onRowClick={handleRowClick}
                />

                {/* Pagination and count */}
                <Paper p="sm" withBorder>
                  <Group justify="space-between">
                    <span>{totalCount} {totalCount === 1 ? 'ticket' : 'tickets'} found</span>
                    <Group gap="xs">
                      <ActionIcon
                        variant="filled"
                        disabled={!previousUrl}
                        onClick={() => previousUrl && fetchTicketes(previousUrl)}
                        aria-label="Previous page"
                      >
                        <IconChevronLeft size={18} />
                      </ActionIcon>
                      <ActionIcon
                        variant="filled"
                        disabled={!nextUrl}
                        onClick={() => nextUrl && fetchTicketes(nextUrl)}
                        aria-label="Next page"
                      >
                        <IconChevronRight size={18} />
                      </ActionIcon>
                    </Group>
                  </Group>
                </Paper>
              </>
            {/* Activity Section - Only show when reach is selected */}
          </Stack>
        </Grid.Col>
      </Grid>
    </Container>
  );
}
